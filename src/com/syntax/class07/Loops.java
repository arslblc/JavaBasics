package com.syntax.class07;

public class Loops {
	
	public static void main (String[] args) {
		
		//how to print 50 to 1 ?
		int num=50;
		while(num>=1) {
			System.out.println(num);
			num--;
		}
		
		
		System.out.println("#####################");
	//how to print adds numbers from 1 to 20?	
		int num1=1;
		while(num1<=20) {
			System.out.println(num1);
			num1++;
		}


		
		
	}

}
